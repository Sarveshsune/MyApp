package com.App;

public class Calculator {
	
	public static int add(int i,int j)
	{
		int result = i+j;
		return result;
	}
	
	public static int sub(int i,int j)
	{
		int result = i-j;
		return result;
	}
	
	public static int mul(int i,int j)
	{
		int result = i*j;
		return result;
	}
	
	public static int div(int i,int j)
	{
		int result = i/j;
		return result;
	}

}
